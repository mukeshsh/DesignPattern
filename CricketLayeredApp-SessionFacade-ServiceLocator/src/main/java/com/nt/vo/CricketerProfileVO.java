package com.nt.vo;

public class CricketerProfileVO {
	private String cricketerId;
	private String cricketerName;
	private  String nation;
	private String  state;
	private String totalRuns;
	private String totalIninings;
	private String battingAvg;
	private String bowlingAvg;
	public String getCricketerId() {
		return cricketerId;
	}
	public void setCricketerId(String cricketerId) {
		this.cricketerId = cricketerId;
	}
	public String getCricketerName() {
		return cricketerName;
	}
	public void setCricketerName(String cricketerName) {
		this.cricketerName = cricketerName;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getTotalRuns() {
		return totalRuns;
	}
	public void setTotalRuns(String totalRuns) {
		this.totalRuns = totalRuns;
	}
	public String getTotalIninings() {
		return totalIninings;
	}
	public void setTotalIninings(String totalIninings) {
		this.totalIninings = totalIninings;
	}
	public String getBattingAvg() {
		return battingAvg;
	}
	public void setBattingAvg(String battingAvg) {
		this.battingAvg = battingAvg;
	}
	public String getBowlingAvg() {
		return bowlingAvg;
	}
	public void setBowlingAvg(String bowlingAvg) {
		this.bowlingAvg = bowlingAvg;
	}
	
}
